/** Returns frost dates for a given US zip code. Defaults to zone 4 if unrecognised. */
export function getFrostDates(zipcode: string): FrostDates {
  const prefix = zipcode.substring(0, 3);
  const zone = ZIP_ZONE[prefix] ?? 4;
  return ZONE_DATES[zone];
}

/**
 * Returns frost dates as Date objects for a specific year.
 * Uses the current year when `year` is not provided.
 */
export function getFrostDateObjects(
  zipcode: string,
  year: number = new Date().getFullYear()
): { lastFrost: Date; firstFrost: Date } {
  const { lastFrost, firstFrost } = getFrostDates(zipcode);
  const [lm, ld] = lastFrost.split('-').map(Number);
  const [fm, fd] = firstFrost.split('-').map(Number);
  return {
    lastFrost: new Date(year, lm - 1, ld),
    firstFrost: new Date(year, fm - 1, fd),
  };
}

/** Formats a Date as MM/DD/YYYY for display. */
export function formatDate(date: Date): string {
  return date.toLocaleDateString('en-US', { month: 'short', day: 'numeric' });
}

/** Parses a YYYY-MM-DD string into a Date at local midnight. */
export function parseIsoDate(iso: string): Date {
  const [y, m, d] = iso.split('-').map(Number);
  return new Date(y, m - 1, d);
}

/** Formats a Date as YYYY-MM-DD for storage. */
export function toIsoDate(date: Date): string {
  const y = date.getFullYear();
  const m = String(date.getMonth() + 1).padStart(2, '0');
  const d = String(date.getDate()).padStart(2, '0');
  return `${y}-${m}-${d}`;
}

/** Adds `days` calendar days to a Date (returns a new Date). */
export function addDays(date: Date, days: number): Date {
  const result = new Date(date);
  result.setDate(result.getDate() + days);
  return result;
}
